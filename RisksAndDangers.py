from dataclasses import dataclass, fields, field
from typing import List, Dict

@dataclass
class RiskTemplate:
    risk_number: str # номер риска
    risk_name: str # название риска
    degree: int # степень тяжести
    degree_info: str # описание степени тяжести
    chance: int # вероятность риска
    chance_info: str # описание вероятности риска
    coefficient: float # коэффициент
    coefficient_info: str # описание коэффициента
    summary: float # итоговый результат,
    summary_info: str
    danger_group_number: int # номер группы опасностей
    danger_group_name: str # название группы опасностей
    control_periodic: str # периодичность контроля
    management_measures: list[str] # меры против риска

@dataclass
class DangerTemplate:
    danger_number: int # номер опасности
    danger_name: str # название опасности
    summary: float # итог по опасности
    risks: List[RiskTemplate] = field(default_factory=list) # риски этой опасности


